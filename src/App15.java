public class App15 {
    public static void main(String[] args) throws Exception {
        int vector[][] = new int[10][10];
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                vector[i][j] = 0;
                System.out.print(vector[i][j]);
            }
            System.out.println(" ");
        }
    }
}
